package cn.zkdcloud.dto;

/**
 * 消息类型
 *
 * @author zk
 * @version 2017/8/20
 */
public enum MsgType {
    /**
     * 文本类型
     */
    TEXT,

    /**
     * 图片类型
     */
    IMAGE,

    /**
     * 语音类型
     */
    VOICE,

    /**
     * 小视频类型
     */
    SHORTVIDEO,

    /**
     * 地理位置类型
     */
    LOCATION,

    /**
     * 链接类型
     */
    LINK,
    /**
     * 音乐类型
     */
    MUSIC,
    /**
     * 事件类型
     */
    EVENT,
    /**
     * 语音消息
     */
    Video,
    /**
     * 图文新闻类型
     */
    NEWS
}
